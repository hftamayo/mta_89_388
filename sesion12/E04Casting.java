package sesion12;

public class E04Casting {
    public static void main(String[] args) {
        // valor en formato doble
        double num = 10.99;
        System.out.println("El valor en formato doble: " + num);
    
        // convesion (casting) a formato entero
        int data = (int)num;
        System.out.println("El valor en formato entero es: " + data);
      }
}
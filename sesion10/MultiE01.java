class MultiE01 {

    public static void main(String[] args) {
        String[][] saludo = {
            {"Sr. ", "Sra. ", "Joven. "},
            {"Marquez"}
        };

        // Sr.
        System.out.println(saludo[0][0] + saludo[1][0]);

        // Sra
        System.out.println(saludo[0][1] + saludo[1][0]);
    }
}


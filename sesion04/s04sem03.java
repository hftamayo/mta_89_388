/*
 ejercicio 3: contar desde un limite superior hasta 1
 utilizando la estructura de control for
 recordar la teoria de limites incluyentes y excluyentes
 
*/

public class s04sem03 {
    static int limiteSuperior = 0;

    public static void main(String args[]){
        for(limiteSuperior = 20; limiteSuperior >= 1; limiteSuperior--){
            System.out.println(limiteSuperior);
        }
    }
    
}